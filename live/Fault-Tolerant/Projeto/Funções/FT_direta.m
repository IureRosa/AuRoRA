function Q = FT_direta(X)
% X = [x;     1
%      y;     2
%      z;     3
%      x1;    4
%      y1;    5
%      z1;    6
%      x2;    7
%      y2;    8
%      z2];   9

p = sqrt((X(4) - X(1))^2 + (X(5) - X(2))^2 + (X(6) - X(3))^2);
q = sqrt((X(7) - X(1))^2 + (X(8) - X(2))^2 + (X(9) - X(3))^2);
r = sqrt((X(4) - X(7))^2 + (X(5) - X(8))^2 + (X(6) - X(9))^2);

Q = [X(1);
    X(2);
    X(3);
    acos(abs(X(6) - X(3))/p);
    atan((X(8) - X(5))/(X(7) - X(4)));
    0;
    p;
    q;
    acos((p^2 + q^2 - r^2)/(2*p*q))];
end

